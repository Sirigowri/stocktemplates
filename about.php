		<H3>NEED ANSWERS????</H3>
        <p>Definition: A stock is a general term used to describe the ownership certificates of any company. A share, on the other hand, refers to the stock certificate of a particular company. Holding a particular company's share makes you a shareholder.</p>
        <p>Description: Stocks are of two types—common and preferred. The difference is while the holder of the former has voting rights that can be exercised in corporate decisions, the later doesn't. However, preferred shareholders are legally entitled to receive a certain level of dividend payments before any dividends can be issued to other shareholders.</p>
        <p>There is also something called 'convertible preferred stock'. This is basically a preferred stock with an option of converting into a fixed number of common shares, usually any time after a predetermined date.</p>
        <p>Stock analysis involves comparing a company’s current financial statement to its financial statements in previous years to give an investor a sense of whether the company is growing, stable, or deteriorating. The financial statement of a company can also be compared to that of one or more other companies within the same industry. A stock analyst may be looking to compare the operating profit margin of two competing companies, by looking at their income statements. The operating profit margin is a metric that shows how much revenue is left after operating expenses have been paid and what proportion of revenue is left to cover non-operating costs, and is calculated as operating income divided by revenue. A company with an operating margin of 0.30 will be looked on more favorably than one with a margin of 0.03. A 0.30 operating margin means that for every dollar of revenue, a company has 30 cents left after operating costs have been covered. In other words, the company uses 70 cents out of every dollar in net sales to pay for its variable or operating costs.</p>
